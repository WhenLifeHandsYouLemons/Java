public class Commenting {
    public static void main(String[] args) {
        System.out.println("");
        System.out.println("There's nothing to see in the terminal. It's all in the code!");
        // This is a way to comment single lines of text
        // This only does it for each line that it's on, no others.
        System.out.println("Hello Sooraj!");
        System.out.println("Let's play hide and seek.");
        System.out.print("Three...");
        System.out.print("Two...");
        System.out.println("One...");
        /*
         * This however, 
         * is a way to comment 
         * multiple lines of text 
         * at once!
         */
        System.out.println("Ready or not, here I come!");
    }
}
